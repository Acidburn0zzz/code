	public static void printHierarchy(Composite mama, int indent){
		int i;
		
		printControl(mama,indent);
		Control[] baby = mama.getChildren();
		indent += 3;
		
		if(baby != null){
			for(i = 0; i < baby.length; i++){
				if(baby[i] instanceof Composite){
					printHierarchy((Composite)baby[i],indent);
				} else {
					printControl(baby[i],indent);
				}
			}
		}
	}
	static private void printControl(Control c, int indent){
		Layout l = null;
		Object ld = null;
		int i;
		
		StringBuffer stb = new StringBuffer();
		for(i = 0; i < indent; i++){
			stb.append(' ');
		}
		if(c instanceof Composite){
			l = ((Composite)c).getLayout();		
		}
		ld = c.getLayoutData();
		System.out.print(stb.toString() + c.toString());
		if(l != null){
			System.out.print(" Layout =" +l.toString());
		}
		if(ld != null){
			System.out.print(" LayoutData =" +ld.toString());
		}
		System.out.println("");
	}
